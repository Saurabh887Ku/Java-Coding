public class Alphabet {
    public static void main(String[] args) {
        System.out.println("Lowercase");
        for(char ch='a'; ch<='z'; ch++){
            System.out.print(ch+" ");
        }
        System.out.println("\nUppercase ");
        for(char ch='A'; ch<='Z'; ch++){
            System.out.print(ch+" ");
        }
    }
}